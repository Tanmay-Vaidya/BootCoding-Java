class AgeClassifier{
      public static void main(String[]args){
      int age=67;

      if(age<12){
      System.out.println("This is child");
      }
      else if(age<18){
      System.out.println("This is Teenager");
      }
      else if(age<34){
      System.out.println("This is Adult");
      }
      else{
      System.out.println("This is Old");
      }
      }
}
          